#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: first int
 * @b: second int
 * Return: operation result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - peforms subtraction on 2 integers
 * @a: first int
 * @b: second int
 * Return: operation result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 * @a: first int
 * @b: second int
 * Return: operation result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - peforms division oon 2 integers
 * @a: first int
 * @b: second int
 * Return: operation result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds modulos 1 integer against another
 * @a: first int
 * @b: second int
 * Return: operation result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
